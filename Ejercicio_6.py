"""EJERCICIO 6.- Calcular el área y el perímetro de un Rectángulo"""

print("Proporciona el alto:")#Se solicita y almacena el valor del alto
alto=int(input())
print("Proporciona el ancho:")#Se solicita y almacena el valor del ancho
ancho=int(input())

perimetro=(2*alto)+(2*ancho)#Se calcula el perímetro
area=alto*ancho#Se calcula el área
 
print("Area:",area)#Se imprime el área
print("Perímetro:",perimetro)#Se imprime el perímetro